<a name="mrules"></a><h2>Rules</h2>

<h3>Ladders</h3>
<ol>
        <li>We currently have three ladders in operation: 3v3, 7v7, 10v10. One registration allows you to participate in any or all ladders.
        Any matches played inside another event or tournament (in 3v3, 7v7, or 10v10) may be reported here, as long as both teams are registered here and agree to the rules
        of that event. Informal scrims don't count unless explicitly agreed or the training room is tagged as a ladder match. The ladders
        are tier agnostic. It is up to the user to determine what tier(s) they wish to play in, either by explicit agreement or by training room choices.
        The default rules are listed here, but teams may agree to any set of rules they wish, so long as they utilize a 3v3 or 7v7 player format.
</ol>

<h3>Game setup</h3>
<ol>
	<li>All matches are best of 5. The proper room settings are 10 minute games (7 mins for 3v3), encounter game type, 
        all classes allowed. The host may set the lobby upper and lower tier limits as they wish (or as they have agreed).</li> 
        <li>Each team may run a maximum of 3 of each tank (1 of each tank in 3v3). Exact reskins (e.g. Lycan,
        Freedom, Black tanks, etc) count as the same tank. Premiums are also limited to one of each tank. AMX
        Chaffee cannot play as top tier. There are no other tank restrictions.</li> 
        <li>Each team will pick a map -- playing once on each side of each map. If the match 
        is tied after 4 games, a single game on Ghost Town will decide the match. The host decides side
        order in all cases.</li>
        <li>Matches may be aborted up until the first game is launched. Note that the hosting
        player is required by the game to either play or spectate.</li>
        <li>You are allowed to play as many matches as you want with any clan that is
        registered on the ladder and which wishes to compete with you.</li> 
        <li>To find an opponent for a ladder game you can either contact clans in the ladder to 
        schedule a match or you may simply create ladder games in the training room and wait for a 
        registered team to join you. You can use the Ladder <a href="https://discord.gg/7e7DUhh">Discord server</a> or use the messaging system here.
	<li>The name of the game <em>must start</em> with the prefix <B>L</B> or
	<B>Ladder</B>. It's recommended but not mandatory to let it be followed by your clan tag,
	your Elo rating, and max tier. The ranking <em>may</em> be rounded to nearest hundredth
	(1545 becomes 1500). An example of a ladder game name according to the above
	would be: <b>Ladder 360NA 1600 T9</b> Another
	example is: <b>L 47R 1800 vs 12PZR 1545 </b> </li>
        <li>All participants <em>must</em> carry the proper clan tag. No exceptions.</li>
	<li>Registered streamers and/or moderators must be spectated. In general, spectators are assumed to be allowed
        unless otherwise stated by either party. If mutually trusted 3rd parties are available
        to spectate, it is encouraged for them to do so. In general people don't cheat, and it's considered to be more polite
	and friendly to allow observers. If cheating does occur, it is usually pretty obvious and spectating should stop for 
        the rest of the match and the game replayed. If their are extra clan members
        spectating, it is recommended for them to mute their mics (or go to a separate party chat) and they should be placed on the 
        opposite side of the training room (if possible), so that they can't ping the map for their team, even inadvertently.</li>
        <li> Please report any clan suspected of cheating so that administrative action may be taken against them.</li>
</ol>

<h3>Picking Tanks</h3>
<ol>
        <li>Teams should not change their tank selections once picked. Players are permitted to
        change equipment/ammo/etc as long as they do it quickly. If a player picks a lower tier than the established max,
        that pick may be changed if done quickly.</li> 
        <li>The guest will signal their wish to begin play by picking the first tank on 
        the first map. Unless previously communicated, whatever tier the guest picks first will indicate the 
        chosen <b>max</b> tier for play (so don't pick a tier 8 light first if you plan to play at tier 9 with your other tanks). 
        Once picking has begun teams should pick tanks quickly. The first team to pick all of its tanks gains the right to change
        1 pick if they wish.</li> 
        <li>After the each game, the winner is expected to initiate tank selection for the next game.</li> 
</ol>

<h3>Scoring</h3>
<ol>
	<li>The <em>winner</em> of the game may report the result to the
	ladder after each finished game. The loser cannot.</li>
	<li>Except for the cases below the winner/loser is the team declared so by WoT.</li>
        <li>If a game <b>ends</b> with one team holding 50 cap points, they win that game.</li>
        <li>In the unlikely event of a draw, the game should be replayed.</li> 
	<li>If a player fails to load into the game for 1 game minute, the game should be halted and restarted (raise barrels and cap or suicide).</li>
        <li>In cases where <em>one</em> a player (or players) leave or get disconnected such that the team no longer
        has 3 players available, the other team must wait 5 minutes for the disconnect
	to return to the training room and continue playing games. <em>If</em> the disconnected player does not return, the team must continue without them.
	<li>In cases where <em>all</em> players get disconnected due to official
	server failure then all players must try to rejoin the server lobby within
	10 minutes from the disconnect in order to resume. If the server is still officially unreachable after 10 minutes, then the game ends
	as a draw and isn't reported to the ladder unless both teams agree on
	something else. If one team reconnects, waits 10 minutes for the other
	team to show up and the other team doesn't, the team who re-connected
	wins the game.</li>
        <li>If a game is accidentally launched in violation of the rules (e.g. wrong time set, invalid tank selection,
        failed to swap sides, etc), you should signal a restart by immediately having your players raise barrels and 
        either move to cap or suicide. If you do not clearly signal a desire to restart at the outset of the
        match, it should be played as is. Note that you can lock your barrel in a given position by holding the right bumper (auto-lock).</li>
        <li>If you manage to make a false report by mistake, you can withdraw it for 7 days or contact us to correct it.</li>
	<li>It's strongly recommended that you always finish a game the same day it
	was started because experience tells us that players you don't know can be
	problematic to get hold of. If however both teams explicitly agree that
	they want to finish the game another time they are allowed to do so. If the
	match is tied then you must wait to finish the match before it is allowed to be reported. However, if one side
	is winning and the other side requests a save to continue later, and then
	you are unable to reach that person for a match within a week you are then
	allowed to go ahead and report the match as a win.</li>
	</li>
</ol>

